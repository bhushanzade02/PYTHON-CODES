# Rules for variable names:
# Rule 1: A variable name must always start with a letter.
# Rule 2: It cannot start with a number.
# Rule 3: Special symbols are not valid in variable names, neither at the start nor in the middle.

# In Python, we use snake_case writing.

# Example:
useronename = "bhushan"
print(useronename)

user_one_name = "bhushan"  # This follows the snake_case convention.
print(user_one_name)


